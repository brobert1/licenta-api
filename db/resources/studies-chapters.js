const loadPgn = (fileName) => getFileContent(`../pgn/${fileName}`);

const buildPgn = ({
  event,
  chapterName,
  studyName,
  opening,
  moves,
  variant = 'Standard',
  result = '*',
  fen,
  setUp,
  remark,
}) => {
  const tags = [
    ['Event', event],
    ['Site', 'Seed Database'],
    ['Result', result],
    ['Variant', variant],
    ['Opening', opening],
    ['StudyName', studyName],
    ['ChapterName', chapterName],
    fen ? ['FEN', fen] : null,
    setUp ? ['SetUp', '1'] : null,
    remark ? ['Remark', remark] : null,
  ].filter(Boolean);

  return `${tags.map(([key, value]) => `[${key} "${value}"]`).join('\n')}\n\n${moves}\n`;
};

